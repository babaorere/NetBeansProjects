    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {

            int reX;
            try {
                reX = Integer.parseInt(txtReX.getText().trim());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Valor real INVALIDO");
                txtReX.requestFocus();
                return;
            }

            int imY;
            try {
                imY = Integer.parseInt(txtImY.getText().trim());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Valor imaginario INVALIDO");
                txtImY.requestFocus();
                return;
            }

            NumComplejo nc = new NumComplejo(reX, imY);

            if (myTreeTDA.Contains(nc)) {
                JOptionPane.showMessageDialog(this, "Número complejo ENCONTRADO: " + nc.toString());
            } else {
                JOptionPane.showMessageDialog(this, "Número complejo NO encontrado: " + nc.toString());
            }

            txtReX.requestFocus();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR \n" + e.toString());
        }

        Limpiar();
        MostrarDatos();

        txtReX.requestFocus();

    }//GEN-LAST:event_btnBuscarActionPerformed


