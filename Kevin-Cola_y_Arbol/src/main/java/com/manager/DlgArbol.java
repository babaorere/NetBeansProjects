    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {

            int reX;
            try {
                reX = Integer.parseInt(txtReX.getText().trim());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Valor real INVALIDO");
                txtReX.requestFocus();
                return;
            }

            int imY;
            try {
                imY = Integer.parseInt(txtImY.getText().trim());
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Valor imaginario INVALIDO");
                txtImY.requestFocus();
                return;
            }

            NumComplejo nc = new NumComplejo(reX, imY);
            myTreeTDA.add(nc);
            myTreeSet.add(nc);

            Rectangle r = pnl.getBounds();
            this.pnlG.remove(pnl);
            this.pnl = new Tree2Graphics(myTreeTDA);
            pnl.setBounds(r);

            this.pnlG.add(this.pnl, java.awt.BorderLayout.CENTER);
            pnlG.repaint();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR \n" + e.toString());
        }

        Limpiar();
        MostrarDatos();

        txtReX.requestFocus();
    }//GEN-LAST:event_btnAddActionPerformed

