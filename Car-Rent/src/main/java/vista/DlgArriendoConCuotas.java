    private void txtPrecioDiaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPrecioDiaKeyTyped
        java.awt.EventQueue.invokeLater(() -> {

            txtMontoPagar.setText("");

            String strDiasArr = txtDiasArr.getText().trim();

            int diasArr;

            try {
                diasArr = Integer.parseInt(strDiasArr);
            } catch (Exception ex) {
                return;
            }

            if (!ArriendoCuota.ValDiasArr(diasArr)) {
                return;
            }

            String strPrecioDia = txtPrecioDia.getText().trim();

            int precioDia;

            try {
                precioDia = Integer.parseInt(strPrecioDia);
            } catch (Exception ex) {
                return;
            }

            if (precioDia <= 0) {
                return;
            }

            txtMontoPagar.setText(String.valueOf(diasArr * precioDia));
        });

    }//GEN-LAST:event_txtPrecioDiaKeyTyped

