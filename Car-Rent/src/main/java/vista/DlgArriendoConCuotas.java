    @Override
    public void actionGuardar_y_Mostrar() {

        arrCuo = null;

        if (lst_sel_cliente.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Cliente.");
            lst_sel_cliente.requestFocus();
            return;
        }

        Cliente c = FrmAppMain.getCliente((String) lst_sel_cliente.getSelectedItem());

        if (lst_sel_vehiculo.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Vehículo.");
            lst_sel_vehiculo.requestFocus();
            return;
        }

        Vehiculo v = FrmAppMain.getVehiculo((String) lst_sel_vehiculo.getSelectedItem());

        String strFechaArr = txtFecArr.getText().trim();
        if (!ArriendoCuota.ValFecArr(strFechaArr)) {
            JOptionPane.showMessageDialog(this, "Fecha de Arriendo invalida.");
            txtFecArr.requestFocus();
            return;
        }

        String strDiasArr = txtDiasArr.getText().trim();

        int diasArr;

        try {
            diasArr = Integer.parseInt(strDiasArr);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Días de Arriendo invalida.");
            txtDiasArr.requestFocus();
            return;
        }

        if (!ArriendoCuota.ValDiasArr(diasArr)) {
            JOptionPane.showMessageDialog(this, "Días de Arriendo invalida.");
            txtDiasArr.requestFocus();
            return;
        }

        String strPrecioDia = txtPrecioDia.getText().trim();

        int precioDia;

        try {
            precioDia = Integer.parseInt(strPrecioDia);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Precio por día invalido.");
            txtPrecioDia.requestFocus();
            return;
        }

        if (precioDia <= 0) {
            JOptionPane.showMessageDialog(this, "Precio por día invalido.");
            txtPrecioDia.requestFocus();
            return;
        }

        String strCantCuo = txtCantCuo.getText().trim();

        int cantidadCuo;

        try {
            cantidadCuo = Integer.parseInt(strCantCuo);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Cantidad de cuotas invalido.");
            txtCantCuo.requestFocus();
            return;
        }

        if ((cantidadCuo <= 0) || (cantidadCuo >= 7)) {
            JOptionPane.showMessageDialog(this, "Cantidad de cuotas invalido.");
            txtCantCuo.requestFocus();
            return;
        }

        arrCuo = new ArriendoCuota(strFechaArr, diasArr, cantidadCuo);

        arrCuo.setCliente(c);
        arrCuo.setVehiculo(v);

        if (!arrCuo.IngresarArriendoConCuotas(precioDia)) {
            JOptionPane.showMessageDialog(this, "Error al tratar de agregar el Arriendo.");
            return;
        }

        if (FrmAppMain.addArriendoCuota(arrCuo)) {

            JOptionPane.showMessageDialog(this, "Arriendo agregado con exito.");
            LimpiarCampos();

            lst_sel_cliente.setEnabled(false);
            lst_sel_vehiculo.setEnabled(false);
            txtFecArr.setEnabled(false);
            txtDiasArr.setEnabled(false);
            txtPrecioDia.setEnabled(false);
            txtMontoPagar.setEnabled(false);
            txtCantCuo.setEnabled(false);

            btnGuardar.setEnabled(false);
            btn_call_ICliente.setEnabled(false);
            btnPagar1raCuota.setEnabled(true);

            DefaultListModel model = (DefaultListModel) lst_cuotas.getModel();
            model.removeAllElements();

            arrCuo.getListCuo().forEach((item) -> {
                model.addElement(item.toString());
            });

        }
    }


    @Override
    public void LimpiarCampos() {
        lst_sel_cliente.setSelectedIndex(0);
        lst_sel_vehiculo.setSelectedIndex(0);
        txtFecArr.setText("");
        txtDiasArr.setText("");
        txtPrecioDia.setText("");
        txtMontoPagar.setText("");
        txtCantCuo.setText("");

        lst_cuotas.setModel(new DefaultListModel());
    }


    @Override
    public void actionPagar1raCuota() {

        if ((arrCuo != null) && arrCuo.Pagar1raCuota()) {
            JOptionPane.showMessageDialog(this, "Primera cuota pagada con exito.");
            controlador.ControlAppMain.actionSalir(this);
        } else {
            JOptionPane.showMessageDialog(this, "Error al tratar de pagar la primera cuota.");
        }

    }
