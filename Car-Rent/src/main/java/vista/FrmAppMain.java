    static ArrayList<String> getListStrFch(Cliente inC) {
        ArrayList<String> aux = new ArrayList<>();

        for (ArriendoCuota item : listArr) {
            if (inC.getId() == item.getCliente().getId()) {
                aux.add(item.getFecArr());
            }
        }

        return aux;
    }


