    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed

        String nickname = txtNickname.getText().trim().toUpperCase();
        if (nickname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Indique el nickname");
            txtNickname.requestFocus();
            return;
        }

        char[] password = txtPassword.getPassword();
        if (password.length <= 0) {
            JOptionPane.showMessageDialog(null, "Indique el password");
            txtNickname.requestFocus();
            return;
        }

        UsuarioCRUD crud = new UsuarioCRUD();

        Optional<Usuario> u = crud.readByNickname(nickname);
        if (!u.isPresent()) {
            JOptionPane.showMessageDialog(null, "Usuario no encontrado");
            txtNickname.requestFocus();
            return;
        }

        String md5Hex = DigestUtils.md5Hex(String.valueOf(password)).toUpperCase();

        if (!u.get().getPassword().equals(md5Hex)) {
            JOptionPane.showMessageDialog(null, "Password invalido");
            txtNickname.requestFocus();
            return;
        }

        this.setVisible(false);
        this.dispose();

        java.awt.EventQueue.invokeLater(() -> {
            new MainWin().setVisible(true);
        });

    }//GEN-LAST:event_btnIngresarActionPerformed

