    private void actualizarTabla() {

        // Borrar la tabla maestra
        tabla.clearSelection();
        final DefaultTableModel model = (DefaultTableModel) tabla.getModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();

        ClienteCRUD cCrud = new ClienteCRUD();
        ServicioCRUD sCrud = new ServicioCRUD();
        BitacoraCRUD bCrud = new BitacoraCRUD();
        ArrayList<Bitacora> list = bCrud.findAll();

        for (Bitacora item : list) {

            Object[] datos = new Object[4];

            String cnombre;
            Optional<Cliente> oc = cCrud.read(item.getIdcliente());
            if (oc.isPresent()) {
                cnombre = oc.get().getNombre();
            } else {
                cnombre = "No encontado";
            }

            String snombre;
            Optional<Servicio> os = sCrud.read(item.getIdservicio());
            if (os.isPresent()) {
                snombre = os.get().getNombre();
            } else {
                snombre = "No encontado";
            }

            datos[0] = item.getIdbitacora();
            datos[1] = cnombre;
            datos[2] = snombre;
            datos[3] = item.getTexto();

            model.addRow(datos);
        }

    }

