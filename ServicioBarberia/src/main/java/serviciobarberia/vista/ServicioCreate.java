    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String nombre = txtxNombre.getText().trim().toUpperCase();
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Indique el nombre");
            txtxNombre.requestFocus();
            return;
        }

        String strPrecio = txtPrecio.getText().trim().toUpperCase();
        if (strPrecio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Indique el precio");
            txtPrecio.requestFocus();
            return;
        }

        Integer precio;
        try {
            precio = Integer.parseInt(strPrecio);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Precio invalido");
            txtPrecio.requestFocus();
            return;
        }

        ServicioCRUD sCrud = new ServicioCRUD();

        Servicio servicio = new Servicio(null, nombre, precio);
        Optional<Servicio> oc = sCrud.create(servicio);
        if (!oc.isPresent()) {
            JOptionPane.showMessageDialog(null, "Servicio no creado");
            txtxNombre.requestFocus();
            return;
        }

        JOptionPane.showMessageDialog(this, "Servicio creado con éxito");

        ActionExit(this);
    }//GEN-LAST:event_btnGuardarActionPerformed

