    @Override
    public void setVisible(boolean b) {

        if (!leerIidusuario) {
            leerIidusuario = true;

            String strNickname = JOptionPane.showInputDialog("Indique el nickname").trim().toUpperCase();

            if (strNickname.isEmpty()) {
                JOptionPane.showConfirmDialog(this, "nickname invalido");
            }

            if (!strNickname.isEmpty()) {

                UsuarioCRUD uCrud = new UsuarioCRUD();

                Optional<Usuario> ou = uCrud.readStr(strNickname);

                if (ou.isPresent()) {
                    usuario = ou.get();
                    txtNickname.setText(usuario.getNickname());
                    txtPassword.setText("");
                } else {
                    usuario = null;
                    txtNickname.setText("");
                    txtPassword.setText("");
                }
            }
        }

        super.setVisible(b);
    }

