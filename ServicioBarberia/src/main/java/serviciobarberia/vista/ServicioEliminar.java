    @Override
    public void setVisible(boolean b) {

        if (b) {
            String strNombre = JOptionPane.showInputDialog("Indique el nombre").trim().toUpperCase();

            if (strNombre.isEmpty()) {
                JOptionPane.showConfirmDialog(this, "nickname invalido");
            }

            if (!strNombre.isEmpty()) {

                ServicioCRUD cCrud = new ServicioCRUD();

                Optional<Servicio> os = cCrud.readStr(strNombre);

                if (os.isPresent()) {
                    servicio = os.get();
                    txtxNombre.setText(servicio.getNombre());
                    txtPrecio.setText(String.valueOf(servicio.getPrecio()));
                } else {
                    JOptionPane.showMessageDialog(this, "Servicio no encontrado");
                    ActionExit(this);
                    return;
                }

            }
        }

        super.setVisible(b);
    }

