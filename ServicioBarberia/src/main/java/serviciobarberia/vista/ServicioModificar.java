    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        if (!modificar) {
            return;
        }

        String nombre = txtxNombre.getText().trim().toUpperCase();
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Indique el nombre");
            txtxNombre.requestFocus();
            return;
        }

        String strPrecio = txtPrecio.getText().trim().toUpperCase();
        if (strPrecio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Indique el precio");
            txtPrecio.requestFocus();
            return;
        }

        Integer precio;
        try {
            precio = Integer.parseInt(strPrecio);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Precio invalido");
            txtPrecio.requestFocus();
            return;
        }

        ServicioCRUD sCrud = new ServicioCRUD();

        servicio.setNombre(nombre);
        servicio.setPrecio(precio);
        Optional<Servicio> oc = sCrud.update(servicio);
        if (!oc.isPresent()) {
            JOptionPane.showMessageDialog(null, "Servicio no actualizado");
            txtxNombre.requestFocus();
            return;
        }

        JOptionPane.showMessageDialog(this, "Servicio modificado con exito");

    }//GEN-LAST:event_btnModificarActionPerformed

