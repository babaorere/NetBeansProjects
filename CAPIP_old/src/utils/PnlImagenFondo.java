    @Override
    public void paintComponent(Graphics g) {
        final Dimension tamanio = getSize();
        URL url = getClass().getResource("/imagenes/fondo azul 22.png");
        if (url == null) {
            return;
        }

        ImageIcon imagenFondo = new ImageIcon(url);
        g.drawImage(imagenFondo.getImage(), 0, 0, tamanio.width, tamanio.height, null);
        setOpaque(false);
        super.paintComponent(g);
    }

