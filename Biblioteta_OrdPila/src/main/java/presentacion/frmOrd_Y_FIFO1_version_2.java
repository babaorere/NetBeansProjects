    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed

        int id;
        String strId = txtId.getText().trim().toUpperCase();

        try {
            id = Integer.parseInt(strId);
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtId.requestFocus();
            return;
        }

        String strAutor;
        strAutor = txtAutor.getText().trim().toUpperCase();

        if (strAutor.length() <= 0) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtAutor.requestFocus();
            return;
        }

        String strTitulo;
        strTitulo = txtTitulo.getText().trim().toUpperCase();

        if (strTitulo.length() <= 0) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtTitulo.requestFocus();
            return;
        }

        int nroCopias;
        String strNroCopias;
        strNroCopias = txtNroCopias.getText().trim().toUpperCase();

        try {
            nroCopias = Integer.parseInt(strNroCopias);
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtNroCopias.requestFocus();
            return;
        }

        TLibro libro = new TLibro(id, strAutor, strTitulo, nroCopias);

        // Verificar si el Libro ya existe
        if (list.contains(libro)) {
            JOptionPane.showMessageDialog(this, "*** EL LIBRO YA FUE INGRESADO ***");
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
        } else {
            list.add(libro);
            VisualizarLista();
        }

        txtId.setText("");
        txtAutor.setText("");
        txtTitulo.setText("");
        txtNroCopias.setText("");

    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        if (list.size() <= 0) {
            JOptionPane.showMessageDialog(this, "***  LISTA VACIA   ***");
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            return;
        }

        int id;
        String strId = txtId.getText().trim().toUpperCase();

        try {
            id = Integer.parseInt(strId);
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            return;
        }

        TLibro libro = new TLibro(id, "", "", 0);

        // Busca el Libro
        int idx = list.indexOf(libro);
        if (idx >= 0) {
            list.remove(idx);
            JOptionPane.showMessageDialog(this, "*** LIBRO BORRADO ***");
            VisualizarLista();
        } else {
            JOptionPane.showMessageDialog(this, "*** LIBRO NO ENCONTRADO ***");
            JOptionPane.showMessageDialog(this, "   *** VALOR INVALIDO ***");
        }

        txtId.setText("");
        txtAutor.setText("");
        txtTitulo.setText("");
        txtNroCopias.setText("");
    }//GEN-LAST:event_btnBorrarActionPerformed

    /**
     * Ver el contenido de la lista en la Tabla
     *
     */
    public void VisualizarLista() {

        CleanTbl();

        for (int i = 0; i < list.size(); i++) {
            Object[] obj = new Object[4];
            obj[0] = list.get(i).getId();
            obj[1] = list.get(i).getAutor();
            obj[2] = list.get(i).getTitulo();
            obj[3] = list.get(i).getNroCopias();

            modelTable.addRow(obj);
        }
    }

