    private void guardarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarClienteActionPerformed

        if (codigo.getText().isEmpty() || nombre.getText().isEmpty() || telefono.getText().isEmpty() || direccion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor llene los espacios en blanco");
        } else {
            escribirRegistro();
            menu.setVisible(true);
            this.setVisible(false);
            this.dispose();

        }

    }//GEN-LAST:event_guardarClienteActionPerformed

    private void escribirRegistro() {
        FileWriter fichero = null;
        PrintWriter pw = null; //Salvar cadena dentro de un txt
        //Instancio porque FileWriter es una clase que captura el txt
        //Las clases estaticas debo instanciarlas, claro ejemplo el FL y PW
        try {

            fichero = new FileWriter("archivos/clientes.txt", true); // si no pongo el true, al tocar el boton de ingresar, sobreescribira el archivo txt
            pw = new PrintWriter(fichero);

            pw.println(this.codigo.getText() + "*" + this.nombre.getText() + "*" + this.telefono.getText() + "*" + this.direccion.getText());

            JOptionPane.showMessageDialog(null, "Agregado con exito");

        } catch (Exception ex) {
            ex.printStackTrace();
        } finally {
            try {
                //uso el finally para asegurarme de que cierre el fichero
                if (null != fichero) {
                    fichero.close();
                }
            } catch (Exception ex2) {
                ex2.printStackTrace();
            }
        }
    }

