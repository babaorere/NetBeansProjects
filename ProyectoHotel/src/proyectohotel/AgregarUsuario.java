    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed

        if (nombrejTextField.getText().isEmpty() || userNamejTextField.getText().isEmpty() || contraseñajTextField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor llene los espacios en blanco");
        } else {
            escribirRegistro();
            if (vienedeLogin == true) {
                registro.setVisible(true);
            } else {
                menu.setVisible(true);
            }
            this.setVisible(false);
            this.dispose();
        }


    }//GEN-LAST:event_agregarActionPerformed

