    /**
     * Rev 23/09/2016
     *
     */
    private void actAceptar() {

        final int idx = cmbCuentas.getSelectedIndex();

        if (idx < 0) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una cuenta");
            return;
        }

        regCuenta = (CuentaModel) listCuenta.get(idx);

        actSalir();
    }

    /**
     * Rev 25/12/2017
     *
     * @param incmbCuentas
     * @param incuenta
     */
    private void cargarComboCuentas() {

        listCuenta.clear();
        cmbCuentas.removeAllItems();
        final DefaultComboBoxModel<String> modeloCombo = new DefaultComboBoxModel<>();

        try (final ResultSet rs = ConnCapip.getInstance().executeQuery("SELECT * FROM bancos ORDER BY banco, cuenta ASC")) {
            while (rs.next()) {
                final CuentaModel regAux = new CuentaModel(rs);
                listCuenta.add(regAux);
                modeloCombo.addElement(regAux.getCuenta() + " " + regAux.getBanco());
            }
        } catch (final Exception inex) {
            JOptionPane.showMessageDialog(null, inex);
            listCuenta.clear();
            modeloCombo.removeAllElements();
            return;
        }

        cmbCuentas.setModel(modeloCombo);
        cmbCuentas.setSelectedIndex(-1);
    }

    /**
     * Rev 25-02-2017
     */
    private void actSalir() {
        final java.awt.Window me = this;
        if (parent != null) {
            java.awt.EventQueue.invokeLater(new Runnable() {
                @Override
                public void run() {

                    if ((Object) parent instanceof CuentaUpdatable) {
                        ((CuentaUpdatable) parent).setCuenta(regCuenta);
                    }

                    parent.setVisible(true);
                    me.dispose();
                }
            });
        } else {
            System.exit(0);
        }
    }

