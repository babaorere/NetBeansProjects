    private void btnRunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRunActionPerformed
        run = true;

        SwingWorker sw1 = new SwingWorker() {

            @Override
            protected String doInBackground() throws Exception {

                while (run) {

                    int comp = random.nextInt(100, 1101);

                    boolean actT1 = comp % (Integer) spnT1.getValue() == 0;

                    final String paqT1 = GenT1();

                    if (actT1) {
                        publish(paqT1);
                    }

                    Thread.sleep(100);
                }

                String res = "Finished Execution";
                return res;
            }

            @Override
            protected void process(List chunks) {
                ((DefaultListModel) lstT2.getModel()).addElement((String) chunks.get(chunks.size() - 1));
            }

            @Override
            protected void done() {
                // this method is called when the background
                // thread finishes execution
                String statusMsg;
                try {
                    statusMsg = (String) get();
                    ((DefaultListModel) lstT2.getModel()).addElement(statusMsg);
                } catch (InterruptedException | ExecutionException ex) {
                    Logger.getLogger(DlgColaPrioridad.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        };

        // executes the swingworker on worker thread
        sw1.execute();

    }//GEN-LAST:event_btnRunActionPerformed

