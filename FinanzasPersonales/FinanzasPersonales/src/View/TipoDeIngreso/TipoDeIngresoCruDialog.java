        if (!registroOpt.isPresent()) {
            return;
        }

        // Validar entradas
        Integer id = registroOpt.get().getId();

        int idxU = cmbUsuario.getSelectedIndex();
        if (idxU < 0) {
            JOptionPane.showMessageDialog(this, "Usuario invalido");
            return; // Salir
        }

