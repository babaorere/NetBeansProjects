    private void mostrarDatos() {

        if ((miCola == null) || miCola.vacia()) {
            limpiarEntradas();
            return;
        }

        DefaultListModel model = new DefaultListModel();
        lstList.setModel(model);

        final int tam = miCola.getTam();
        for (int i = 0; i < tam; i++) {

            Telefono elem = miCola.eliminar();

            model.addElement(elem.toString());

            // Encolar de nuevo el elemento
            // Como se trata de un for fijo, al final obtenemos los elementos ordenados de la misma forma  
            miCola.insertar(elem);
        }

        txtTam.setText(String.valueOf(miCola.getTam()));
        txtIni.setText(miCola.getInicio().toString());
        txtFin.setText(miCola.getFinal().toString());

        txtNom.requestFocus();
    }


    private void limpiarEntradas() {
        txtTam.setText("");
        txtIni.setText("");
        txtFin.setText("");
        txtNom.setText("");
        txtTelf.setText("");

        txtNom.requestFocus();
    }


