    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed

        // Lista no ha sido creada
        if (miCola == null) {
            JOptionPane.showMessageDialog(null, "La cola no ha sido Creada");
            limpiarEntradas();
            txtNom.requestFocus();
            return;
        }

        String nombre = txtNom.getText().trim().toUpperCase();

        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtNom.requestFocus();
            return;
        }

        String strNumero = txtTelf.getText().trim().toUpperCase();

        if (strNumero.length() != 9) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtTelf.requestFocus();
            return;
        }

        // Solo para verificar que es un Entero valido        
        try {
            BigInteger foo = new BigInteger(strNumero);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtTelf.requestFocus();
            return;
        }

        miCola.insertar(new Telefono(nombre, strNumero));

        limpiarEntradas();
        mostrarDatos();
        txtNom.requestFocus();
    }//GEN-LAST:event_btnInsertarActionPerformed

