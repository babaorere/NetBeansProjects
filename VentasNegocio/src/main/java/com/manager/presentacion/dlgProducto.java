    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed

        int codigo;
        String strCodigo = txtCodigo.getText().trim().toUpperCase();

        try {
            codigo = Integer.parseInt(strCodigo);
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtCodigo.requestFocus();
            return;
        }

        if (codigo <= 0) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtCodigo.requestFocus();
            return;
        }

        String strDesc;
        strDesc = txtDesc.getText().trim().toUpperCase();

        if (strDesc.length() <= 0) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtDesc.requestFocus();
            return;
        }

        int stock;
        String strStock = txtStock.getText().trim().toUpperCase();

        try {
            stock = Integer.parseInt(strStock);
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtStock.requestFocus();
            return;
        }

        if ((stock <= 0) || (stock > 1_000_000)) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtStock.requestFocus();
            return;
        }

        int precio;
        String strPrecio = txtPrecio.getText().trim().toUpperCase();

        try {
            precio = Integer.parseInt(strPrecio);
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtStock.requestFocus();
            return;
        }

        if ((precio <= 0) || precio > 1_000_000) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtStock.requestFocus();
            return;
        }

        String strUniMedida;
        strUniMedida = txtStock.getText().trim().toUpperCase();

        if (strUniMedida.length() <= 0) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtUniMedida.requestFocus();
            return;
        }

        // Aqui se utiliza la lista dinamica
        TProducto.InsertarLista(new TProducto.TNodo(new TProducto(codigo, strDesc, stock, precio, strUniMedida)));
        LimpiarEntradas();
        VerDatos();
        Resumen();

    }//GEN-LAST:event_btnInsertarActionPerformed

    private void LimpiarEntradas() {
        txtCodigo.setText("");
        txtDesc.setText("");
        txtStock.setText("");
        txtPrecio.setText("");
        txtUniMedida.setText("");
    }

    private void VerDatos() {

        DefaultTableModel model = (DefaultTableModel) tblTable.getModel();
        model.setRowCount(0); // Borrar la tabla

        final TProducto.TNodo head = TProducto.getHead();

        if (head != null) {
            TProducto.TNodo p = head;
            while (p != null) {
                TProducto aux = p.getProducto();
                Object[] fila = {aux.getCodigo(), aux.getDescripción(), aux.getStock(), aux.getPrecio()};
                model.addRow(fila);
                p = p.getEnlaceSig();
            }
        }
    }

