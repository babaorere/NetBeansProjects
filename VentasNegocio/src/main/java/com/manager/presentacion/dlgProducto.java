    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        int codigo;
        String strCodigo = txtCodigo.getText().trim().toUpperCase();

        try {
            codigo = Integer.parseInt(strCodigo);
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtCodigo.requestFocus();
            return;
        }

        if (codigo <= 0) {
            JOptionPane.showMessageDialog(this, "*** VALOR INVALIDO ***");
            txtCodigo.requestFocus();
            return;
        }

        TProducto.TNodo pFound = TProducto.Buscar(codigo);
        if (pFound != null) {
            VerNodoActual(TProducto.getNodoActual());
            VerDatos();
        } else {
            JOptionPane.showMessageDialog(this, "El codigo: no esta en la lista");
            LimpiarEntradas();
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarActionPerformed

    /**
     * Muestra en el panel de componentes, los valores del nodo
     *
     * @param nodo
     */
    private void VerNodoActual(TProducto.TNodo nodo) {
        if (nodo != null) {
            txtCodigo.setText(String.valueOf(nodo.getProducto().getCodigo()));
            txtDesc.setText(nodo.getProducto().getDescripción());
            txtStock.setText(String.valueOf(nodo.getProducto().getStock()));
            txtPrecio.setText(String.valueOf(nodo.getProducto().getPrecio()));
            txtUniMedida.setText(String.valueOf(nodo.getProducto().getUnidadMedida()));
        } else {
            LimpiarEntradas();
        }
    }

